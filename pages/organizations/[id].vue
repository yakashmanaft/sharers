    );
    let usersInBand = [];

    if (indexArray.length) {
      indexArray.forEach((sharer) => {
        if (users.value) {
          let userObj = [...users.value].find(
            (user) => user.id === sharer.userID
          );

          usersInBand.push(userObj);
        }
      });
    }
    return usersInBand.sort((x, y) => {
      if (x.surname < y.surname) {
        return -1;
      }

      if (x.surname > y.surname) {
        return 1;
      }
    });
  }
});
